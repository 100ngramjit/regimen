// ── Markdown export ─────────────────────────────────────────────────────────

export function workoutToMarkdown(workout: Workout): string {
  const lines: string[] = [
    `# ${workout.title}`,
    `**Duration:** ${workout.totalTime} minutes`,
    '',
  ];
  for (const section of workout.sections) {
    lines.push(`## ${section.name}`);
    lines.push('');
    lines.push('| Exercise | Reps / Duration | Rest | Notes |');
    lines.push('|----------|----------------|------|-------|');
    for (const ex of section.exercises) {
      const metric = ex.reps ? `${ex.reps} reps` : ex.duration || '—';
      const notes = ex.instructions || '—';
      lines.push(`| ${ex.name} | ${metric} | ${ex.rest} | ${notes} |`);
    }
    lines.push('');
  }
  return lines.join('\n');
}

export function weeklyPlanToMarkdown(plan: WeeklyPlan): string {
  const lines: string[] = [
    `# ${plan.weekTitle}`,
    `**Goal:** ${plan.goal}`,
    '',
    '---',
    '',
  ];
  for (const day of plan.days) {
    if (day.isRest) {
      lines.push(`## ${day.day} — 🛌 Rest Day`);
      lines.push('');
      lines.push('*Recovery & muscle repair. Stay hydrated and get quality sleep.*');
      lines.push('');
    } else if (day.workout) {
      lines.push(`## ${day.day} — ${day.focus}`);
      lines.push('');
      lines.push(workoutToMarkdown(day.workout).replace(/^# .+\n/, '').trim());
      lines.push('');
    }
    lines.push('---');
    lines.push('');
  }
  lines.push(`*Generated by FitForge AI — ${new Date().toLocaleDateString()}*`);
  return lines.join('\n');
}

// ── Plain Text export ────────────────────────────────────────────────────────

export function workoutToText(workout: Workout): string {
  const lines: string[] = [
    `FITFORGE WORKOUT: ${workout.title.toUpperCase()}`,
    `Duration: ${workout.totalTime} minutes`,
    '='.repeat(40),
    '',
  ];
  for (const section of workout.sections) {
    lines.push(`[ ${section.name.toUpperCase()} ]`);
    lines.push('');
    for (const ex of section.exercises) {
      const metric = ex.reps ? `${ex.reps} reps` : ex.duration || '—';
      lines.push(`${ex.name.padEnd(25)} | ${metric.padEnd(12)} | Rest: ${ex.rest}`);
      if (ex.instructions) lines.push(`  Notes: ${ex.instructions}`);
      lines.push('');
    }
  }
  return lines.join('\n');
}

export function weeklyPlanToText(plan: WeeklyPlan): string {
  const lines: string[] = [
    `FITFORGE WEEKLY PLAN: ${plan.weekTitle.toUpperCase()}`,
    `Goal: ${plan.goal}`,
    '='.repeat(40),
    '',
  ];
  for (const day of plan.days) {
    lines.push(`--- ${day.day.toUpperCase()} ---`);
    if (day.isRest) {
      lines.push('REST DAY: Neural Recovery & Muscle Repair');
    } else if (day.workout) {
      lines.push(`FOCUS: ${day.focus}`);
      lines.push('');
      lines.push(workoutToText(day.workout).split('='.repeat(40))[1]?.trim() || '');
    }
    lines.push('');
  }
  lines.push(`Generated by FitForge AI on ${new Date().toLocaleDateString()}`);
  return lines.join('\n');
}

