type ExArg = { name: string; sets?: string | null; reps?: string | null; duration?: string | null; rest: string; instructions?: string | null };

function cleanRest(rest: string): string {
  if (!rest || rest === 'N/A' || rest === 'n/a' || rest === '—') return '';
  return rest;
}

function renderExerciseCard(ex: ExArg, idx: number): string {
  const setsHtml  = ex.sets ? `<span style="${S.badgeSets}">${ex.sets} SETS</span>` : '';
  // Truncate very long rep strings, always append unit label
  const rawReps     = ex.reps ?? '';
  const rawDuration = ex.duration ?? '';
  const shortReps     = rawReps.length > 14 ? rawReps.slice(0, 13) + '…' : rawReps;
  const shortDuration = rawDuration.length > 14 ? rawDuration.slice(0, 13) + '…' : rawDuration;
  const targetHtml = rawReps
    ? `<span style="${S.badgeReps}">${shortReps} REPS</span>`
    : rawDuration
      ? `<span style="${S.badgeReps}">${shortDuration}</span>`
      : '';
  const rest = cleanRest(ex.rest);
  const restHtml = rest ? `<span style="${S.badgeRest}"><span style="${S.restIcon}">⏱</span> REST ${rest}</span>` : '';

  return `
<div style="${S.exCard}">
  <div style="${S.exTable}">
    <div style="${S.exLeft}">
      <span style="${S.numBadge}">${idx + 1}</span>
      <span style="${S.exNameWrap}">
        <div style="${S.exName}">${ex.name}</div>
        ${ex.instructions ? `<div style="${S.exNote}">${ex.instructions}</div>` : ''}
      </span>
    </div>
    <div style="${S.exRight}">
      ${setsHtml}${targetHtml}
      ${restHtml}
    </div>
  </div>
</div>`;
}

type SectionArg = { name: string; exercises: ExArg[] };

function renderSections(sections: SectionArg[]): string {
  return sections.map(section => {
    const exercises = section.exercises.map((ex, i) => renderExerciseCard(ex, i)).join('');
    return `
<div style="${S.divWrap}">
  <div style="${S.divLine}"></div>
  <div style="${S.divLabel}">${section.name}</div>
  <div style="${S.divLine}"></div>
</div>
${exercises}`;
  }).join('');
}

